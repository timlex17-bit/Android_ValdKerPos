package com.example.valdker.models;

public class Shop {

    public int id;
    public String name;
    public String address;
    public String phone;
    public String email;
    public String logoUrl;
    public String allCategoryIconUrl;

    public Shop() {

        this.id = id;
        this.name = name;
        this.address = address;
        this.phone = phone;
        this.email = email;
        this.logoUrl = logoUrl;
        this.allCategoryIconUrl = allCategoryIconUrl;
    }
}
